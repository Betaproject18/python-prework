# sorting lists temporarily

alist = ['apples','bananas','pears','oranges','apricots']
print(sorted(alist))

# sorting in alphabetical order

alist = ['apples','bananas','pears','oranges','apricots']
alist.sort()
print(alist)

#reverse alphabetical sorting
alist = ['apples','bananas','pears','oranges','apricots']
alist.sort(reverse=True)
print(alist)

