# Examples of how looping works with for VALUE in THE PLACE YOU WANT IT TO LOOK
magicians = ['alice','david','caroline']
for magician in magicians:
    print(magician)

#this tells python to retrieve the value magician in magicians, which prints them all out as python goes
#in a loop until the last value is retrieved and then printed

for value in range(1,5):
    print(value)

